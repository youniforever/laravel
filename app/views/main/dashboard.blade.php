<h2>Laravel 철학</h2>

<p>Laravel은 풍부하고 우아한 구문을 제공하는 웹어플리케이션 프레임워크 입니다. 우리는 개발이 즐거워야 하고, 진심으로 성취감을 주는 창의적인 체험이 되어야 한다고 믿습니다. Laravel은 인증, 라우팅, 세션, 캐싱같이 대부분의 웹 프로젝트에서 사용되는 일반적인 작업들을 구현하여 개발할때 이러한 고통을 없애려고 시도합니다.</p>

<p>Laravel은 개발과정이 어플리케이션의 희생없이 개발자에게 즐겁도록 하는것이 목표입니다. 행복한 개발자가 최상의 코드를 만듭니다. 이를 위해, Ruby on Rails, ASP.NET MVC, Sinatra 같은 다른 언어의 프레임워크에서 본 최고의 것들을 결합수 있도록 시도 했습니다.</p>

<p>Laravel은 크고, 강력한 어플리케이션에 필요한 강력한 툴로 사용 될 수 있습니다. 컨트롤러 컨테이너의 강력한 도치, 표현적인 마이그레이션 시스템, 깔끔하게 통합된 유닛 테스팅은 당신이 만들어야 할 모든 어플리케이션을 구축하는데 필요한 툴들을 제공합니다.</p>

